package javabean;

public class ShoppingRecord {
	private String record_id;
	private String goods_name;
	private String goods_price;
	private String goods_num;
	private String goods_offer;
	private String goods_buyer;
	public String getRecord_id() {
		return record_id;
	}
	public void setRecord_id(String record_id) {
		this.record_id = record_id;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public String getGoods_price() {
		return goods_price;
	}
	public void setGoods_price(String goods_price) {
		this.goods_price = goods_price;
	}
	public String getGoods_num() {
		return goods_num;
	}
	public void setGoods_num(String goods_num) {
		this.goods_num = goods_num;
	}
	public String getGoods_offer() {
		return goods_offer;
	}
	public void setGoods_offer(String goods_offer) {
		this.goods_offer = goods_offer;
	}
	public String getGoods_buyer() {
		return goods_buyer;
	}
	public void setGoods_buyer(String goods_buyer) {
		this.goods_buyer = goods_buyer;
	}
	
}
