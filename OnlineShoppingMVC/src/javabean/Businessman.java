package javabean;

public class Businessman {
	private String business_id;
	private String business_name;
	private String business_password;
	private String business_money;
	public String getBusiness_id() {
		return business_id;
	}
	public void setBusiness_id(String business_id) {
		this.business_id = business_id;
	}
	public String getBusiness_name() {
		return business_name;
	}
	public void setBusiness_name(String business_name) {
		this.business_name = business_name;
	}
	public String getBusiness_password() {
		return business_password;
	}
	public void setBusiness_password(String business_password) {
		this.business_password = business_password;
	}
	public String getBusiness_money() {
		return business_money;
	}
	public void setBusiness_money(String business_money) {
		this.business_money = business_money;
	}
	
}
